package Fundamentos;

public class Artmeticos {
	public static void main(String[] args) {
		
		//Operações aritméticas:
		
		
		//Adição:
		System.out.println(3+4);
		
		var x = 34.56;
		double y = 35.44;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x / y);
		System.out.println(x * y);
		
		int a = 8;
		int b = 3;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a / b);
		System.out.println(a * (double)b);
	}
}
